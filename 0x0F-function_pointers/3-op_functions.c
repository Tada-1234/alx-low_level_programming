#include "3-calc.h"
/**
 * op_add - adds
 * @a: value
 * @b: value
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a+b);
}

/**
* op_sub - subtracts
* @a: value
* @b: value
*
* Return: difference
*/
int op_sub(int a, int b)
{
	return (a-b);
}

/**
* op_mul - multiplies
* @a: value
* @b: value
*
* Return: product
*/
int op_mul(int a, int b)
{
	return (a*b);
}

/**
* op_div - divides
* @a: value
* @b: value
*
* Return: quotient
*/
int op_div(int a, int b)
{
	return (a/b);
}

/**
* op_mod - finds remainder
* @a: value
* @b: value
*
* Return: modulus
*/
int op_mod(int a, int b)
{
	return (a%b);
}
